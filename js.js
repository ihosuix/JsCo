// operator | ++ | --

//prefix Increment | پیش افزایش
let a = 10;
console.log(++a);

// prefix Decrement | پیش کاهش
let b = 10;
console.log(--b);

//postfix Increment | پس افزایش
let c = 10;
console.log(c++);

//postfix Decrement | پس کاهش
let d = 10;
console.log(d--);


//example

//prefix Increment
let e = 20;
console.log(e);  //خروجی 20
console.log(++e); // خروجی 21 
console.log(e);  // خروجی 21

//postfix Increment 
let g = 40;
console.log(g); // خروجی 40
console.log(g++); //
console.log(g);
console.log(g * 2);

//prefix Decrement
let f = 30;
console.log(f);
console.log(--f);
console.log(f);


//postfix Decrement
let h = 50;
console.log(h);
console.log(h--);
console.log(h);
console.log(h * 2);













