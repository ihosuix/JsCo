// ساختار های شرطی | if , else , else if , switch

// if | عبارت شرطی درصورتی که درست باشد اجرا می شود
// ساختار if
if (condition) {
    // کدی که باید اجرا شود
}

// example | true
let age = 20;
if (age > 18) {
    console.log("شما بزرگسال هستید");
}

// example // false
let age = 17; 
if (age > 18) {
    console.log("شما بزرگسال هستید");
}
