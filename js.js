// Comparison Operators
// اپراتور های مقایسه ای

// == | علامت برابر بودن
console.log(2 == 2); //T
console.log(3 == 2); //F


// != | علامت نامساوی بودن
console.log(2 != 2); //F
console.log(3 != 2); //T


// > | بزرگتر
console.log(2 > 2); //F
console.log(3 > 2); //T


// < | کوچکتر 
console.log(2 < 2); //F
console.log(3 < 2); //F


// >= | بزرگتر مساوی
console.log(2 >= 2); //T
console.log(3 >= 2); //T


// <= | کوچکتر مساوی
console.log(2 <= 2); //T
console.log(3 <= 2); //F


